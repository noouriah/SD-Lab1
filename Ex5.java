//Define a method to print array elements
public class Ex5 {
    public static void main(String[] args) {
        int []a={1,2,3,4,6,8};
        output(a);
    }
    public static void output(int[] arr){
        for (int i=0;i<arr.length;i++){
            System.out.println(arr[i]);
        }
    }
}
